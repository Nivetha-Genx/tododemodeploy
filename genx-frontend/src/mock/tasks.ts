export const getTaskById = (id: string): Task | undefined => {
    return mockTasks.find(task => task.id === id)
}

export const getTasksByBoardId = (boardId: string): Task[] => {
    return mockTasks.filter(task => task.boardId === boardId)
}

export const getTasksByStatus = (status: StatusType): Task[] => {
    return mockTasks.filter(task => task.status === status)
}

export const getTasksByAssignee = (assigneeId: string): Task[] => {
    return mockTasks.filter(task => task.assigneeId === assigneeId)
}

export const getTodaysTasks = (userId: string): Task[] => {
    const today = new Date().toISOString().split('T')[0]
    return mockTasks.filter(task =>
        task.assigneeId === userId &&
        task.status !== 'completed' &&
        task.dueDate >= today
    )
}

export const getOverdueTasks = (userId?: string): Task[] => {
    const today = new Date().toISOString().split('T')[0]
    return mockTasks.filter(task =>
        task.status !== 'completed' &&
        task.dueDate < today &&
        (!userId || task.assigneeId === userId)
    )
}

export const getPendingRequests = (): DueDateChangeRequest[] => {
    return mockDueDateRequests.filter(req => req.status === 'pending')
}

export const getCompletedTasks = (userId?: string): Task[] => {
    return mockTasks.filter(task =>
        task.status === 'completed' &&
        (!userId || task.assigneeId === userId)
    )
}

export const getUpcomingTasks = (userId?: string): Task[] => {
    const today = new Date().toISOString().split('T')[0]
    const nextWeek = new Date(Date.now() + 7 * 24 * 60 * 60 * 1000).toISOString().split('T')[0]

    return mockTasks.filter(task =>
        task.status !== 'completed' &&
        task.dueDate > today &&
        task.dueDate <= nextWeek &&
        (!userId || task.assigneeId === userId)
    )
}

export const getInboxTasks = (userId: string): Task[] => {
    return mockTasks.filter(task =>
        task.assigneeId === userId &&
        task.status !== 'completed'
    )
}

